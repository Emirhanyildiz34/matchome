  /// Şehire ait üniversiteleri döndürür
  static List<String> getByCity(String city) =>
      universitiesByCity[city] ?? [];

  /// Belirli üniversitenin kampüs adları listesi
  static List<String> getCampusNames(String university) =>
      campusesByUniversity[university] ?? ['Merkez Kampüs'];

  /// Kampüs arama (üniversite bağlamında)
  static List<String> searchCampuses(String university, String query) {
    final campuses = getCampusNames(university);
    if (query.isEmpty) return campuses;
    final q = query.toLowerCase();
    return campuses.where((c) => c.toLowerCase().contains(q)).toList();
  }

  /// Tüm üniversiteleri düz liste olarak döndürür
  static List<({String name, String city})> get allUniversities {
    final result = <({String name, String city})>[];
    for (final entry in universitiesByCity.entries) {
      for (final uni in entry.value) {
        result.add((name: uni, city: entry.key));
      }
    }
    result.sort((a, b) => a.name.compareTo(b.name));
    return result;
  }

  /// Üniversite adı veya şehre göre arama yapar
  static List<({String name, String city})> search(String query) {
    final q = query.toLowerCase();
    return allUniversities
        .where((u) =>
            u.name.toLowerCase().contains(q) ||
            u.city.toLowerCase().contains(q))
        .toList();
  }
